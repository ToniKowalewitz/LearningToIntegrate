function [poles, coefficients, absterm] = get_poles(n, varargin)
    % Tabulate RBA poles/coefficients for exp(-x) on (0, +INF)
    %
    % SYNTAX
    %   [poles, coefficients, absterm] = get_poles(n, 'include_conjugate', true)
    %
    % INPUT PARAMETER
    %   n ... number of poles (counting complex conjugate poles)
    %
    % OPTIONAL PARAMETER
    %   include_conjugate ... bool (default true); if false, complex
    %                         conjugates are not returned, one pole
    %                         is representing each pair, correspoding
    %                         coefficient is doubled
    %
    % OUTPUT PARAMETER
    %   poles        ... complex column vector
    %   coefficients ... complex column vector
    %   absterm      ... complex scalar

    % Parse name-value arguments
    p = inputParser();
    p.addParameter('include_conjugate', true);
    p.parse(varargin{:});
    include_conjugate = p.Results.include_conjugate;

    % Tabulate RBA for exp(-x) on (0,+INF)
    [poles, coefficients, absterm] = polres_Remez(n);

    % NB: Below we assume the ordering of the poles and coefficients,
    %     i.e., first come complex conjugate pairs, each pair in
    %     in succession

    % Get rid of numeric noise in imaginary part of the real pole
    if mod(n, 2) == 1
        poles(end) = real(poles(end));
        coefficients(end) = real(coefficients(end));
    end

    % Get rid of complex conjugate pairs
    if ~include_conjugate

        % Select odd poles
        poles = poles(1:2:end);

        % Extract odd, even, and real coefficients
        coeffs_paired_1 = coefficients(1:2:end-1);
        coeffs_paired_2 = coefficients(2:2:end);
        if mod(n, 2) == 0
            coeff_real = [];
        else
            coeff_real = coefficients(end);
        end

        % Sum contributions
        coeffs_paired = coeffs_paired_1 + conj(coeffs_paired_2);

        % Sanity check
        assert(all(coeffs_paired == 2*coeffs_paired_1));
        assert(all(coeffs_paired == 2*conj(coeffs_paired_2)));

        % Merge pairs and real
        coefficients = vertcat(coeffs_paired, coeff_real);
    end

    % Flip ordering of the poles (for better numerical stability?!?)
    poles = flipud(poles);
    coefficients = flipud(coefficients);

    % Sanity check
    assert(iscolumn(poles) && iscolumn(coefficients));
    if ~include_conjugate
        n = idivide(n, uint32(2), 'ceil');
    end
    assert(numel(poles) == n && numel(coefficients) == n);
    assert(isscalar(absterm) && isreal(absterm));
end


